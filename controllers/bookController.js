// Handle book create on POST.
exports.book_create_post = [
    // Convert the genre to an array.
    (req, res, next) => {
        
        
        // console.log("req.body author", req.body.author);
        // console.log("req.body genre", req.body.genre);
        // Using these you can see what the the Form (book_form.pug) "returns" 
        // "returns" == *stores in req.body variables and call the appropriate route to manage* 
        // req.body.author is always returned as a string
        // req.body.genre is returned a string if one is selected and an array if 2 or more are selected
        // However, it needs to be an array before being sent to the database, because genre feild in the book table is of type array

        // So if it's not an array, i.e. only one genre selected - convert to an array before proceeding
        if(!(req.body.genre instanceof Array)){
            if(typeof req.body.genre ==='undefined')
            req.body.genre = [];
            else
            req.body.genre = new Array(req.body.genre);
        }
        // console.log("req.body author", req.body.author);
        // console.log("req.body genre", req.body.genre);
        next();
    },

    // Validate and sanitize fields.
    body('title', 'Title must not be empty.').trim().isLength({ min: 1 }).escape(),
    body('author', 'Author must not be empty.').trim().isLength({ min: 1 }).escape(),
    body('summary', 'Summary must not be empty.').trim().isLength({ min: 1 }).escape(),
    body('isbn', 'ISBN must not be empty').trim().isLength({ min: 1 }).escape(),
    body('genre.*').escape(),

    // Process request after validation and sanitization.
    (req, res, next) => {

        // Extract the validation errors from a request.
        const errors = validationResult(req);

        // Create a Book object with escaped and trimmed data.
        var book = new Book(
          { title: req.body.title,
            author: req.body.author,
            summary: req.body.summary,
            isbn: req.body.isbn,
            genre: req.body.genre
           });


        // console.log("rfsfsdr", typeof req.body.author)
        

        if (!errors.isEmpty()) {
            // There are errors. Render form again with sanitized values/error messages.

            // Get all authors and genres for form.
            async.parallel({
                authors: function(callback) {
                    Author.find(callback);
                },
                genres: function(callback) {
                    Genre.find(callback);
                },
            }, function(err, results) {
                if (err) { return next(err); }
                //console.log("book testing stuff:", book.genre.indexOf(0), typeof book.genre.indexOf(0), book.genre[0].toString(), typeof book.genre[0].toString);

                // Mark our selected genres as checked.
                for (let i = 0; i < results.genres.length; i++) {
                    // The indexOf() method returns the first index at which a given element can be found in the array, or -1 if it is not present.
                    // I guess this is actually the best way to avoid an "undefined" crash.. clever
                    if (book.genre.indexOf(results.genres[i]._id) > -1) {
                        results.genres[i].checked='true';
                    }
                }
                res.render('book_form', { title: 'Create Book', authors: results.authors, genres: results.genres, book: book, errors: errors.array() });
            });
            return;
        }
        else {
            // Data from form is valid. Save book.
            book.save(function (err) {
                if (err) { return next(err); }
                   //successful - redirect to new book record.
                   res.redirect(book.url);
                });
        }
    }
];


